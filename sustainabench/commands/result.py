from typing import Annotated
import typer
import json
from rich import print
from pathlib import Path
from sustainabench.reporting.result_processor import ResultProcessor

app = typer.Typer()

@app.command()
def generate(
    indicator_names: Annotated[list[str], typer.Option(..., "--indicator", "-i", help="Which indicators to derive from the raw measurements after the workload has been completed (multiple allowed)")],
    results_file: Annotated[Path, typer.Option(..., "--results", "-r", help="File containing benchmarking results, either generated by this suite or another. Please ensure, if results are generated by another benchmark, that they are in the correct format (see README.md)")],
    output_dir: Annotated[Path, typer.Option(..., "--output", "-o", help="Benchmark output directory")] = Path("./experiments/results/"),
    config_file: Annotated[Path, typer.Option(..., "--config", "-c", help="Path to the config file for the benchmark")] = Path("")
):
    """Command to generate results based on benchmarking results"""

    raw_results = None

    with results_file.open("w", encoding="utf-8") as f: # If other benchmarks can export to this format, then further analysis can be done using 'sustainabench generate' on third-party results
        raw_results = json.load(f)
    
    processor = ResultProcessor(raw_results, indicator_names)

    results = processor.process()

    print("Results:")
    print(results)

    output_dir.mkdir(parents=True, exist_ok=True)
    filename = f"{results_file.stem}___{'-'.join(indicator_names)}.json"
    output_file = output_dir / filename

    with output_file.open("w", encoding="utf-8") as f: # If other benchmarks can export to this format, then further analysis can be done using 'sustainabench generate' on third-party results
        json.dump(results, f, indent=4, ensure_ascii=False)
        print("Outputted results to:", output_file)
