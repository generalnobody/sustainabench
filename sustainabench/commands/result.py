from typing import Annotated
import typer
import json
import yaml
from pathlib import Path
from sustainabench.reporting.result_processor import ResultProcessor
from sustainabench.schemas.results.benchmark import BenchmarkResult
from sustainabench.schemas.configs.metrics import MetricConfig
from sustainabench.schemas.results.metrics_dict import MetricsDict

app = typer.Typer()

@app.command()
def generate(
    results_file: Annotated[Path, typer.Option(..., "--results", "-r", help="File containing benchmarking results, either generated by this suite or another. Please ensure, if results are generated by another benchmark, that they are in the correct format (see README.md)")],
    metric_names: Annotated[list[str], typer.Option(..., "--metric", "-i", help="Which metrics to derive from the raw measurements after the workload has been completed (multiple allowed). Some (e.g. carbon), may require files to function correctly. Provide files separated with '=', e.g. carbon=traces/carbon/traces/trace.parquet.")],
    metrics_dict_file: Annotated[Path, typer.Option(..., "--metrics-dict", "-md", help="Path to a config file (YAML) that specifies JSON paths to standardized element names for metrics (e.g. J, kWh, time, ...)")],
    output_dir: Annotated[Path, typer.Option(..., "--output", "-o", help="Benchmark output directory")] = Path("./experiments/results/"),
    config_file: Annotated[Path, typer.Option(..., "--config", "-c", help="Path to the config file for the benchmark")] = Path("")
):
    """Command to generate results based on benchmarking results"""
    # Right now, with how filenames are passed, cannot use "all". Probably, for this, need to create separate metric, that is able to contain all other metrics and have it be passed a file that contains mapping for all other metrics.
    # if "all" in metric_names:
    #     metric_names = list(METRICS.keys())

    with open(metrics_dict_file) as f:
        raw_metrics_dict = yaml.safe_load(f)
    metrics_dict = MetricsDict.model_validate(raw_metrics_dict)
    
    processor = ResultProcessor(metric_names, metrics_dict) # Initialize processor

    with results_file.open("r", encoding="utf-8") as f: # If other benchmarks can export to this format, then further analysis can be done using 'sustainabench generate' on third-party results
        raw = json.load(f)

    raw_results = BenchmarkResult.model_validate(raw)

    raw_metric_cfg = None
    if config_file != Path(""):
        with open(config_file) as f:
            raw_metric_cfg = yaml.safe_load(f)

    metric_cfg = None
    if raw_metric_cfg:
        metric_cfg = MetricConfig.model_validate(raw_metric_cfg, context={ "allowed_metrics": processor.get_loaded_metrics() }) # Validate imported config

    results = processor.process(raw_results, metric_cfg).model_dump()

    print("Results:")
    print(json.dumps(results, indent=4))

    output_dir.mkdir(parents=True, exist_ok=True)
    filename = f"{results_file.stem}___{'-'.join(processor.get_loaded_metrics())}.json"
    output_file = output_dir / filename

    with output_file.open("w", encoding="utf-8") as f: # If other benchmarks can export to this format, then further analysis can be done using 'sustainabench generate' on third-party results
        json.dump(results, f, indent=4, ensure_ascii=False)
        print("Outputted results to:", output_file)
