from typing import Annotated
import typer
import json
import yaml
from rich import print
from pathlib import Path
from sustainabench.reporting.result_processor import ResultProcessor
from sustainabench.indicators import INDICATORS

app = typer.Typer()

@app.command()
def generate(
    results_file: Annotated[Path, typer.Option(..., "--results", "-r", help="File containing benchmarking results, either generated by this suite or another. Please ensure, if results are generated by another benchmark, that they are in the correct format (see README.md)")],
    indicator_names: Annotated[list[str], typer.Option(..., "--indicator", "-i", help="Which indicators to derive from the raw measurements after the workload has been completed (multiple allowed). Some (e.g. carbon), may require files to function correctly. Provide files separated with '=', e.g. carbon=traces/carbon/traces/trace.parquet.")],
    output_dir: Annotated[Path, typer.Option(..., "--output", "-o", help="Benchmark output directory")] = Path("./experiments/results/"),
    config_file: Annotated[Path, typer.Option(..., "--config", "-c", help="Path to the config file for the benchmark")] = Path("")
):
    """Command to generate results based on benchmarking results"""
    # Right now, with how filenames are passed, cannot use "all". Probably, for this, need to create separate indicator, that is able to contain all other indicators and have it be passed a file that contains mapping for all other metrics.
    # if "all" in indicator_names:
    #     indicator_names = list(INDICATORS.keys())
    
    processor = ResultProcessor(indicator_names) # Initialize processor

    raw_results = None
    with results_file.open("r", encoding="utf-8") as f: # If other benchmarks can export to this format, then further analysis can be done using 'sustainabench generate' on third-party results
        raw_results = json.load(f)

    if raw_results is not None:
        if "workload" not in raw_results:
            raise ValueError("Missing 'workload' key in results")
        if "node_results" not in raw_results:
            raise ValueError("Missing 'node_results' key in results")
        for node in raw_results["node_results"]:
            if "metrics" not in node:
                raise ValueError("Missing 'metrics' key in results")

    indicator_cfg = None
    if config_file != Path(""):
        with open(config_file) as f:
            indicator_cfg = yaml.safe_load(f)

    if indicator_cfg is not None:
        loaded_indicators = processor.get_loaded_indicators()
        if "indicators" not in indicator_cfg:
            raise ValueError("Missing 'indicators' key in config")
        if len(indicator_cfg["indicators"]) == 0:
            raise ValueError("Config files requires at least one item to be present under main 'indicators' key")
        for ind in indicator_cfg["indicators"]:
            if ind not in loaded_indicators:
                raise ValueError(f"Unknown indicator key '{ind}'. Maybe you misspelled?")
            if "params" not in indicator_cfg["indicators"][ind]:
                raise ValueError(f"Missing 'params' key under '{ind}' key in config")

    results = processor.process(raw_results, indicator_cfg)

    print("Results:")
    print(results)

    output_dir.mkdir(parents=True, exist_ok=True)
    filename = f"{results_file.stem}___{'-'.join(processor.get_loaded_indicators())}.json"
    output_file = output_dir / filename

    with output_file.open("w", encoding="utf-8") as f: # If other benchmarks can export to this format, then further analysis can be done using 'sustainabench generate' on third-party results
        json.dump(results, f, indent=4, ensure_ascii=False)
        print("Outputted results to:", output_file)
